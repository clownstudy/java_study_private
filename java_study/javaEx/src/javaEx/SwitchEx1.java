package javaEx;

public class SwitchEx1 {
	public static void main(String[] args) {
	/* 
		switch(조건)--값(정수,char){
		case(값1): // break;가 안들어가면 아래로 다 돌기 때문에, 웬만하면 break를 쓰는 것이...
		case(값2):
		case(값3):
		default:
		}
	*/	
		int i[] = {1,2,3,4,5};
		for(int ii:i) {
			switch(ii) {
			case 1 :
				System.out.println("1입니다.");
				break;
			case 2 :
				System.out.println("2입니다.");
				break;
			case 3 :
				System.out.println("3입니다.");
				break;
			case 4 :
				System.out.println("4입니다.");
				break;
			// case 5 :
				// System.out.println("5입니다.");
				// break;
			default:
				System.out.println("5입니다.");
			}
		}
	}
}
